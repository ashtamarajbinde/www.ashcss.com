
let days;
days = 3;
switch (days) {
    case 1:
        console.log('first day is monday')
        break;
    case 2:
        console.log('second day is tuesday')
        break;
    case 3:
        console.log('third day is wednesday')
        break;
    case 4:
        console.log('fourth day is thursday')
        break;
    case 5:
        console.log('fifth day is friday ')
        break;
    case 6:
        console.log('sixth day is saturday')
        break;
    case 7:
        console.log('seventh day is sunday')
        break;
    default:
        console.log('invalid choice')
}